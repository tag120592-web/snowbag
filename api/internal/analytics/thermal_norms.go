// Нормативные данные теплотехнического расчёта из СП 50.13330.2024.
// Извлечено из текста СП (страницы указаны в комментариях). Числа подлежат
// проверке инженером. Здесь — только справочные данные и простые выборки;
// сам расчёт — в thermal.go.

// Коэффициенты теплоотдачи поверхностей плоской кровли (покрытия).
// СП 50, табл. 4 (αв) и табл. 6 (αн): αв = 8,7; αн(зима) = 23.
const (
	Rsi = 1.0 / 8.7 // сопротивление теплоотдаче внутренней поверхности ≈ 0,115 (м²·°C)/Вт
	Rse = 1.0 / 23.0 // сопротивление теплоотдаче наружной поверхности ≈ 0,043 (м²·°C)/Вт
)

// GSOP — градусо-сутки отопительного периода, °C·сут/год (СП 50, формула 5.2):
// ГСОП = (tв − tот)·zот, где tот, zот — по СП 131 (период со среднесут. t ≤ 8 °C).
func GSOP(tIn, tOt float64, zOt int) float64 {
	return (tIn - tOt) * float64(zOt)
}

// roofRtrPoint — узловые значения Rтр покрытий по ГСОП (СП 50, табл. 3, графа «Покрытий»).
type roofRtrPoint struct {
	gsop float64
	rtr  float64
}

// Категории зданий соответствуют прототипу/ТЗ:
//  "1" Жилые, гостиницы, общежития           → СП 50 поз. 1.1
//  "2" Детсады, школы, медицинские, интернаты → СП 50 поз. 1.2
//  "3" Общественные, административные, бытовые → СП 50 поз. 2
//  "4" Производственные (сухой/нормальный)    → СП 50 поз. 3
//  "5" Производственные с избытком теплоты     → СП 50: индивидуально (примечание 2); MVP — как поз. 3
//
// Для поз. 1.1/1.2 покрытий задана линейная зависимость Rтр = a·ГСОП + b (a=0,0005; b=2,2).
// Для поз. 2/3 покрытий заданы табличные точки — интерполируем.
var roofRtrTable = map[string][]roofRtrPoint{
	"3": {{1000, 1.5}, {2000, 2.0}, {4000, 2.8}, {6000, 3.4}, {8000, 3.9}, {10000, 4.4}, {12000, 4.8}},
	"4": {{1000, 1.5}, {2000, 2.0}, {4000, 2.8}, {6000, 3.4}, {8000, 3.9}, {10000, 4.4}, {12000, 4.8}},
	"5": {{1000, 1.5}, {2000, 2.0}, {4000, 2.8}, {6000, 3.4}, {8000, 3.9}, {10000, 4.4}, {12000, 4.8}},
}

// RequiredRoofResistance — требуемое (базовое) сопротивление теплопередаче покрытия
// Rтр, (м²·°C)/Вт, по СП 50 табл. 3 в зависимости от категории здания и ГСОП.
func RequiredRoofResistance(categoryID string, gsop float64) float64 {
	switch categoryID {
	case "1", "2": // линейная зависимость для покрытий
		return 0.0005*gsop + 2.2
	default:
		pts := roofRtrTable[categoryID]
		if pts == nil {
			pts = roofRtrTable["4"] // безопасный дефолт — производственные
		}
		return interpRtr(pts, gsop)
	}
}

// interpRtr — линейная интерполяция Rтр по узловым точкам; вне диапазона —
// крайние значения (СП 50, примечание 1 к табл. 3: свыше 12000 — как для 12000).
func interpRtr(pts []roofRtrPoint, gsop float64) float64 {
	if len(pts) == 0 {
		return 0
	}
	if gsop <= pts[0].gsop {
		return pts[0].rtr
	}
	last := pts[len(pts)-1]
	if gsop >= last.gsop {
		return last.rtr
	}
	for i := 1; i < len(pts); i++ {
		if gsop <= pts[i].gsop {
			a, b := pts[i-1], pts[i]
			k := (gsop - a.gsop) / (b.gsop - a.gsop)
			return a.rtr + k*(b.rtr-a.rtr)
		}
	}
	return last.rtr
}

// DefaultIndoorTemp — расчётная температура помещения по умолчанию для категории, °C
// (минимальные значения оптимальной температуры; решение «авто по категории + ручная
// правка»). TODO(ГОСТ 30494 / СП): уточнить значения по категориям/режимам.
func DefaultIndoorTemp(categoryID string) float64 {
	switch categoryID {
	case "1": // жилые, гостиницы, общежития
		return 20
	case "2": // детсады, школы, медицинские, интернаты
		return 21
	case "3": // общественные, административные, бытовые
		return 20
	case "4": // производственные сухой/нормальный
		return 16
	case "5": // производственные с избытком теплоты
		return 16
	default:
		return 20
	}
}

// NormalizedTempDiff — нормируемый температурный перепад Δtн для покрытий, °C
// (СП 50, табл. 5), для проверки температуры внутренней поверхности.
// Для производственных (сухой/нормальный) норматив 0,8·(tв−tр), но не более 6 —
// вычисляется в thermal.go по точке росы; здесь дан верхний предел.
func NormalizedTempDiff(categoryID string) float64 {
	switch categoryID {
	case "1", "2": // жилые, детсады/школы, медицинские, интернаты
		return 3.0
	case "3": // общественные, административные, бытовые
		return 4.0
	case "4": // производственные сухой/нормальный — не более 6 (точное значение по tр)
		return 6.0
	case "5": // производственные с избытком явной теплоты
		return 12.0
	default:
		return 4.0
	}
}
