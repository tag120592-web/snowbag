// Генератор затронутых статей (для демонстрации масштаба в сотни статей).
function buildPages(n, prefix, sections) {
  const out = [];
  for (let i = 1; i <= n; i++) {
    const sec = sections[i % sections.length];
    out.push({ title: `${prefix} № ${i}`, path: sec });
  }
  return out;
}

